package Variables_and_Data_Types;

class JavaVariablesDataTypes{
    public static void main(String[] argc) {
        // Declaring a variable and initializing it with a value
            int age = 23;
            String name = "khalid";

        // Printing the value of the variables
            System.out.println("Name: " + name);
            System.out.println("Age: " + age);

        // Changing the value of a variable
            age = 60;
            name = "FIFEL";

        // Printing the new values of the variables
            System.out.println("Name: " + name);
            System.out.println("Age: " + age);

    }
}